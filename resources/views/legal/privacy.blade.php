<li>Die weiteren Daten (Email-Adresse, Ernährungsweise, PLZ) werden nicht automatisch gelöscht, da sie für eine weitere Camp-Anmeldung genutzt werden können. Die Daten können im persönlichen Profil aber jederzeit selbst geändert oder gelöscht werden</li>
</ul>
</li>
