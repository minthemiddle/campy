</li>
<li>
<p><em>Welche Daten?</em> Wir speichern folgende Daten: Vorname, Nachname, Email-Adresse, Geburtsdatum, Ernährungsweise, PLZ, digitale Unterschrift</p>
</li>
<li>
<p><em>Welche zusätzlichen Daten bei Minderjährigen?</em> Bei Minderjährigen erheben wir zusätzlich Vorname, Nachname, Email-Adresse,  Telefonnummer und digitale Unterschrift eines Erziehungsberechtigten</p>
</li>
<li>
<p><em>Welche Daten nicht?</em> Wir speichern Information zum Geschlecht nur als statistische Wahrscheinlichkeitsableitung vom Vornamen per Gender-API.com ab.</p>
</li>
<li>
<p><em>Wofür?</em></p>
<ul>
<li>
<em>Vorname und Nachname:</em> Zurordnung von Zahlungsvorgängen</li>
<li>
<em>Email-Adresse:</em> Versand von Informationen zum Camp, ggf. Hinweis auf weitere Camps in der Nähe in der Zukunft</li>
<li>
<em>Geburtsdatum:</em> Feststellung, ob Teilnehmer Bestätigung der Erziehungsberechtigten benötigt</li>
<li>
<em>Ernährungsweise:</em> Zur Bestellung des Caterings ist es notwendig, die Anzahl an Sonderdiäten zu kennen</li>
<li>
<em>PLZ:</em> Benötigt für kommunal gebundene Förderung (etwa durch die Arbeitsagentur oder Städte)</li>
<li>
<em>Unterschrift</em>: Rechtssichere Bestätigung der Anmeldung und Teilnahme</li>
<li>
<em>Daten der Erziehungsberechtigten:</em> Kontaktaufnahme a, zur rechtssichere Bestätigung der Einwilligung und Teilnahmebedingungen und b, bei Problemen während der Campzeit</li>
</ul>
</li>
